# Routes
# ---------------------------------------------------------------------------

@app.route("/submit", methods=["POST"])
def submit():
    data = request.get_json(silent=True) or {}
    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "").strip()

    if not text:
        return jsonify({"error": "Missing required field: text"}), 400
    if not creator_id:
        return jsonify({"error": "Missing required field: creator_id"}), 400

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    # Signal 1 — only signal wired in for this milestone
    llm_score = groq_signal(text)

    # Placeholders — wired in properly in Milestone 4
    confidence = llm_score
    attribution = "likely_ai" if llm_score >= 0.65 else (
        "likely_human" if llm_score < 0.35 else "uncertain"
    )
    label = "placeholder - label generator added in Milestone 5"

    log_entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "attribution": attribution,
        "confidence": round(confidence, 3),
        "llm_score": round(llm_score, 3),
        "stylometric_score": None,  # added in Milestone 4
        "status": "classified",
    }
    write_log(log_entry)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": round(confidence, 3),
        "label": label,
    })


@app.route("/log", methods=["GET"])
def log():
    return jsonify({"entries": get_log()})


if __name__ == "__main__":
    app.run(debug=True, port=5000)